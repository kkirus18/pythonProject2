    def __init__(self):
        """Инициализация генератора."""
        self.questions_count = len(self.QUESTION_BANK)

    def get_random_questions(self, n: int) -> List[Dict[str, Any]]:
        """
        Случайная выборка N вопросов из базы.

        Аргументы:
            n: количество вопросов для выборки

        Возвращает:
            список случайно выбранных вопросов
        """
        if n > self.questions_count:
            n = self.questions_count
        return random.sample(self.QUESTION_BANK, n)

    def shuffle_options(self, question: Dict[str, Any]) -> Tuple[Dict[str, Any], Dict[int, int]]:
        """
        Перемешивание вариантов ответов для вопроса.

        Аргументы:
            question: словарь с вопросом

        Возвращает:
            кортеж (скопированный_вопрос_с_перемешанными_вариантами, словарь_маппинга_индексов)
        """
        # Создаём копию вопроса
        shuffled_q = question.copy()

        # Получаем исходные вариант и правильный индекс
        original_options = question["options"].copy()
        original_correct = question["correct"]
        original_correct_text = original_options[original_correct]

        # Создаём пары (индекс, текст) и перемешиваем
        indexed_options = list(enumerate(original_options))
        random.shuffle(indexed_options)

        # Составляем новые варианты и маппинг старых индексов на новые
        new_options = []
        old_to_new = {}
        new_to_old = {}

        for new_idx, (old_idx, text) in enumerate(indexed_options):
            new_options.append(text)
            old_to_new[old_idx] = new_idx
            new_to_old[new_idx] = old_idx

        shuffled_q["options"] = new_options
        shuffled_q["correct"] = old_to_new[original_correct]
        shuffled_q["correct_text"] = original_correct_text

        return shuffled_q, new_to_old